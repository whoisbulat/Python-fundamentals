# Базовые арифметические операции
print(10 + 2)   # 12
print(10 - 4)   # 6
print(3 * 5)    # 15
print(10 / 3)   # 3.333...
print(10 // 3)  # 3
print(10 % 3)   # 1 Остаток от деления
print(2 ** 4)   # 16 Возведение в степень


# Операции сравнения
print(10 == 10)  # True
print(10 != 5)   # True
print(7 > 3)     # True
print(7 < 3)     # False
print(5 >= 5)    # True


# Присваивание с операцией
x = 10
x += 5   # x = 15
x -= 3   # x = 12
x *= 2   # x = 24
x //= 5  # x = 4
x **= 3  # x = 64



