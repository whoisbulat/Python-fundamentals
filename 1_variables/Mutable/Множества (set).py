###########1. Создание множеств###########

# Пустое множество
s = set()
print(s)  # set()

# Из списка (дубли удаляются)
s = set([1, 2, 2, 3])
print(s)  # {1, 2, 3}

# Из строки (уникальные символы)
s = set("hello")
print(s)  # {'h', 'e', 'l', 'o'}

# Литеральная форма
s = {1, 2, 3}
print(s)  # {1, 2, 3}



###########2. Базовые операции###########

s = {1, 2, 3}

# Размер множества
print(len(s))  # 3

# Проверка принадлежности
print(2 in s)  # True
print(5 not in s)  # True

# Итерация
for item in s:
    print(item)  # 1 2 3 (порядок не гарантирован)




###########3 Добавление и удаление элементов###########

s = {1, 2, 3}

# Добавление
s.add(4)
print(s)  # {1, 2, 3, 4}

# Удаление
s.remove(2)  # Вызовет KeyError если элемента нет
print(s)  # {1, 3, 4}

s.discard(5)  # Не вызывает ошибку при отсутствии
print(s)  # {1, 3, 4}

# Удаление и возврат случайного элемента
popped = s.pop()
print(popped)  # 1 (или другой элемент)
print(s)  # {3, 4}

# Очистка
s.clear()
print(s)  # set()




########### 4 Операции с другими множествами###########

s1 = {1, 2, 3}
s2 = {3, 4, 5}

# Объединение
print(s1.union(s2))  # {1, 2, 3, 4, 5}
print(s1 | s2)  # Аналогично

# Пересечение
print(s1.intersection(s2))  # {3}
print(s1 & s2)  # Аналогично

# Разность
print(s1.difference(s2))  # {1, 2}
print(s1 - s2)  # Аналогично

# Симметричная разность
print(s1.symmetric_difference(s2))  # {1, 2, 4, 5}
print(s1 ^ s2)  # Аналогично




########### 5. Сравнение множеств ###########

s1 = {1, 2}
s2 = {1, 2, 3}

# Подмножество
print(s1.issubset(s2))  # True
print(s1 <= s2)  # Аналогично

# Собственное подмножество
print(s1 < s2)  # True

# Надмножество
print(s2.issuperset(s1))  # True
print(s2 >= s1)  # Аналогично

# Непересекающиеся множества
print(s1.isdisjoint({4, 5}))  # True




###########6. Взаимодействие с другими типами ###########

# Из списка в множество
lst = [1, 2, 2, 3]
s = set(lst)
print(s)  # {1, 2, 3}

# Из множества в список
lst = list(s)
print(lst)  # [1, 2, 3] (порядок не гарантирован)

# Из строки
s = set("hello")
print(s)  # {'h', 'e', 'l', 'o'}

# Из кортежа
t = (1, 2, 3)
s = set(t)
print(s)  # {1, 2, 3}

# Из словаря (только ключи)
d = {'a': 1, 'b': 2}
s = set(d)
print(s)  # {'a', 'b'}




########### 7. Копирование множеств###########

s = {1, 2, 3}

# Поверхностная копия
s_copy = s.copy()
print(s_copy)  # {1, 2, 3}

# Глубокая копия (если элементы изменяемы)
import copy
deep_copy = copy.deepcopy(s)




########### 9. Генераторы множеств###########

# Аналогично списковым включениям
s = {x**2 for x in range(5)}
print(s)  # {0, 1, 4, 9, 16}