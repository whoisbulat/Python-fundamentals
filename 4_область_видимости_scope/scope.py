# nonlocal:
# –ü–æ–∑–≤–æ–ª—è–µ—Ç –∏–∑–º–µ–Ω—è—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ –≤–Ω–µ—à–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏


def counter():
    count = 0

    def increment():
        nonlocal count  # –£–∫–∞–∑—ã–≤–∞–µ–º, —á—Ç–æ count - –∏–∑ –≤–Ω–µ—à–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏
        count += 1

    increment()
    print(count)  # 1


counter()



